#include "libft.h"

int	ft_isnumeric(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if ((str[0] < '0' || str[0] > '9') && (str[0] != '-' && str[0] != '+'))
			return (0);
		if ((str[0] == '-' && str[0] == '+') && (str[1] < '0' || str[1] > '9'))
		   return (0);	
		if (i != 0 && (str[i] < '0' || str[i] > '9'))
			return (0);
		i++;
	}
	return (1);
}
